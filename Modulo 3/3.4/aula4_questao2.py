nota_usuario = int(input("de uma nota para o filme de 1 a 5"))

if nota_usuario == 1:
    print("Ruim")
elif nota_usuario == 2:
    print("Regular")
elif nota_usuario == 3:
    print("Bom!")
elif nota_usuario == 4:
    print("Muito Bom!")
elif nota_usuario == 5:
    print("Exelente!")
else:
    print("Avaliação invalida,insira uma avaliação de 1 a 5.")